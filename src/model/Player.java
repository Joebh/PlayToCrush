package model;

/**
 *
 * @author Joebh
 */
public class Player {
    
    private double x, y;

    public Player() {
    }
    
    public Player(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
    
    
    
}
